// 色板数据
export const colorPalettes: Record<BeadBrand, ColorPalette> = {
  hama: { brand: 'hama', colors: hamaColors },
  perler: { brand: 'perler', colors: perlerColors },
  artkal: { brand: 'artkal', colors: artkalColors },
  nabbi: { brand: 'nabbi', colors: hamaColors }, // Nabbi 使用 Hama 色系
}

// 获取指定品牌的色板
export function getColorPalette(brand: BeadBrand): BeadColor[] {
  return colorPalettes[brand]?.colors || hamaColors
}

// 获取所有品牌的颜色列表
export function getAllColors(): BeadColor[] {
  return [...hamaColors, ...perlerColors, ...artkalColors]
}

// 根据颜色ID查找颜色
export function getColorById(id: string): BeadColor | undefined {
  return getAllColors().find((c) => c.id === id)
}

// 根据品牌和色号查找颜色
export function getColorByCode(brand: BeadBrand, code: string): BeadColor | undefined {
  return getColorPalette(brand).find((c) => c.code === code)
}
